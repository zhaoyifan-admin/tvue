import { openBlock, createElementBlock, toDisplayString } from 'vue';

function render(_ctx, _cache, $props, $setup, $data, $options) {
  return (openBlock(), createElementBlock("span", null, toDisplayString(_ctx.end), 1 /* TEXT */))
}

export { render };
